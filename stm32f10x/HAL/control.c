void motor_mod(void)
{
	switch(mod)
	{
		case 1:
			Analysis_wires();	break;				//解析wires中的数据,并控制电机
		case 2:
			Analysis_hcsro4();  break;                //超声避障 ,并控制电机
	}
}


void Analysis_wires(void)   //巡线解析控制函数
{
	if(wires & LINE_INIT)
	{
		if(wires & STRAIGHT_LINE)
		{
			printf("0\r\n");  
		}
		if(wires & SLIGHTLY_LEFT)
		{
			printf("-1\r\n");  
		}
		if(wires & SLIGHTLY_RIGHT)
		{
			printf("1\r\n");  
		}
		if(wires & TURN_LEFT)
		{
			printf("-2\r\n");  
		}
		if(wires & TURN_RIGHT)
		{
			printf("2\r\n");  
		}
	}else{
		//printf("电机未初始化\r\n");
		return;
	}
	
}


void Analysis_hcsro4(void)                  //超声避障解析
{
	if(hcsr04_flag)
	{
		//wires &= ~LINE_INIT; 
		switch(hcsr04_flag)
		{
			case HCSR04_IS_LE:
				 printf("右转\r\n");
				break;
			case HCSR04_IS_RI:
				printf("左转\r\n");
				break;
			case HCSR04_IS:
				printf("左或右转\r\n");
				break;
			case HCSR04_IS_RI_LE:
				printf("停止\r\n");
				break;
		}

	}

}
